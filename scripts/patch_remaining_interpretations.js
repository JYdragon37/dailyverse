async function main() {
  const db_ref = db.collection('verses');
  let count = 0;

  for (const [id, patch] of Object.entries(PATCHES)) {
    await db_ref.doc(id).update(patch);
    count++;
    console.log(`✅ ${id} 수정 완료`);
  }

  console.log(`\n총 ${count}개 verse 해석 업데이트 완료`);

  // 검증
  console.log('\n--- 검증 중 ---');
  let remaining = 0;
  const snap = await db_ref.get();
  snap.forEach(doc => {
    const interp = doc.data().interpretation || '';
    if (/히브리어|헬라어|"[가-힣\w]{2,8}"는/.test(interp)) {
      remaining++;
      console.log(`⚠️  [${doc.id}]`, interp.substring(0, 80));
    }
  });
  console.log(`\n남은 문제: ${remaining}개`);
  process.exit(0);
}

main().catch(e => { console.error(e); process.exit(1); });
