async function updateVerses() {
  const ids = Object.keys(updates);
  let successCount = 0;
  let failCount = 0;

  for (const id of ids) {
    try {
      await db.collection('verses').doc(id).update({
        interpretation: updates[id].interpretation,
        application: updates[id].application
      });
      console.log(`✅ ${id} 업데이트 완료`);
      successCount++;
    } catch (err) {
      console.error(`❌ ${id} 업데이트 실패:`, err.message);
      failCount++;
    }
  }

  console.log(`\n완료: 성공 ${successCount}개, 실패 ${failCount}개`);
  process.exit(0);
}

updateVerses().catch(e => { console.error(e); process.exit(1); });
