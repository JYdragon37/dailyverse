async function updateAll() {
  const ids = Object.keys(verses).sort();
  let success = 0;
  let fail = 0;

  for (const id of ids) {
    try {
      await db.collection('alarm_verses').doc(id).update({
        interpretation: verses[id].interpretation,
        application: verses[id].application
      });
      console.log(`✅ ${id} 업데이트 완료`);
      success++;
    } catch (e) {
      console.error(`❌ ${id} 실패:`, e.message);
      fail++;
    }
  }

  console.log(`\n완료: 성공 ${success}개 / 실패 ${fail}개`);
}

updateAll().then(() => process.exit(0)).catch(e => { console.error(e); process.exit(1); });
