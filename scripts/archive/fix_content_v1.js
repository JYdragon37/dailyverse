async function run() {
  let successCount = 0;
  let totalInterpLen = 0;
  let totalAppLen = 0;

  for (const u of updates) {
    try {
      const updateData = {};
      if (u.interpretation) {
        updateData.interpretation = u.interpretation;
      }
      if (u.application) {
        updateData.application = u.application;
      }

      // 길이 검증
      const iLen = (updateData.interpretation || '').replace(/\n/g, '').length;
      const aLen = (updateData.application || '').replace(/\n/g, '').length;

      if (updateData.interpretation && iLen < 150) {
        console.warn(`WARNING: ${u.id} interpretation too short: ${iLen}`);
      }
      if (updateData.application && aLen < 60) {
        console.warn(`WARNING: ${u.id} application too short: ${aLen}`);
      }

      await db.collection('verses').doc(u.id).update(updateData);
      successCount++;
      if (updateData.interpretation) totalInterpLen += iLen;
      if (updateData.application) totalAppLen += aLen;
      console.log(`Updated ${u.id} (${u.ref}) — i:${iLen} a:${aLen}`);
    } catch (e) {
      console.error(`Failed ${u.id}:`, e.message);
    }
  }

  console.log('\n===== 완료 보고 =====');
  console.log(`수정된 항목 수: ${successCount} / ${updates.length}`);
  console.log(`평균 interpretation 길이: ${Math.round(totalInterpLen / successCount)}`);
  console.log(`평균 application 길이: ${Math.round(totalAppLen / successCount)}`);
  process.exit(0);
}

run().catch(e => { console.error(e); process.exit(1); });
