async function main() {
  const ids = Object.keys(updates);
  console.log(`총 ${ids.length}개 업데이트 시작...`);

  for (const id of ids) {
    try {
      await db.collection('verses').doc(id).update(updates[id]);
      console.log(`✅ ${id} 업데이트 완료`);
    } catch (e) {
      console.error(`❌ ${id} 업데이트 실패:`, e.message);
    }
  }

  console.log('\n모든 업데이트 완료!');
  process.exit(0);
}

main().catch(e => { console.error(e); process.exit(1); });
