async function main() {
  console.log(`총 ${Object.keys(PATCHES).length}개 말씀 패치 시작...`);

  const batch = db.batch();
  let count = 0;

  for (const [verseId, patch] of Object.entries(PATCHES)) {
    const ref = db.collection('verses').doc(verseId);
    batch.update(ref, patch);
    count++;
    console.log(`  [${verseId}] 업데이트 예약됨`);
  }

  await batch.commit();
  console.log(`\n✅ ${count}개 말씀 해석/적용 업데이트 완료`);

  // 검증: 업데이트 후 히브리어/헬라어 포함 여부 재확인
  console.log('\n검증 중...');
  const snapshot = await db.collection('verses').get();
  let remaining = 0;
  snapshot.forEach(doc => {
    const d = doc.data();
    const interp = d.interpretation || '';
    if (interp.includes('히브리어') || interp.includes('헬라어') || interp.includes('히브리')) {
      console.log(`  ⚠️  아직 원어 포함: [${doc.id}] ${d.reference}`);
      remaining++;
    }
  });

  if (remaining === 0) {
    console.log('✅ 모든 원어 표현이 제거되었습니다.');
  } else {
    console.log(`\n⚠️  ${remaining}개 말씀에 여전히 원어 표현이 남아 있습니다.`);
  }

  process.exit(0);
}

main().catch(e => { console.error(e); process.exit(1); });
