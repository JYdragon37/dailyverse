(async () => {
  const ids = Object.keys(updates).sort();
  let successCount = 0;
  let failCount = 0;

  for (const id of ids) {
    try {
      const data = updates[id];
      // 글자 수 확인
      const interpLen = data.interpretation.replace(/\n/g, '').length;
      const appLen = data.application.replace(/\n/g, '').length;
      console.log(`${id} | 해석: ${interpLen}자 | 적용: ${appLen}자`);

      await db.collection('alarm_verses').doc(id).update({
        interpretation: data.interpretation,
        application: data.application,
      });
      console.log(`  ✓ 업데이트 완료`);
      successCount++;
    } catch (err) {
      console.error(`  ✗ 실패: ${err.message}`);
      failCount++;
    }
  }

  console.log(`\n완료: 성공 ${successCount}개, 실패 ${failCount}개`);
  process.exit(0);
})();
